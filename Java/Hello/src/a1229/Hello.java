package a1229;

public class Hello {
    public static void main(String[] args) {
        System.out.println("Hello, World");
        // Java 실행되면 제일 먼저 main 메서드를 실행한다.
        // Public 은 접근제어자 - 외부에서 접근 할 수 있다.
        // private -> protected -> public
        // static 정적 메서드 -> 객체를 만들지 않고도 사용가능
        // 공통으로 하나마나 존재
        // 메모리에 한 번만 생성
        // void 는 반환값이 없다.
    }
}
/* 여러줄 주석 시 사용 */